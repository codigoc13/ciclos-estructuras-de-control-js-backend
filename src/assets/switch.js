const dia = 0 //el 0 es el domingo

switch (dia) {
  case 0:
    console.log('domingo')
    break
  case 1:
    console.log('lunes')
    break
  case 2:
    console.log('martes')
    break
  case 3:
    console.log('miércoles')
    break
  default:
    console.log('No es domingo, ni lunes, ni martes, ni miércoles')
}
